def plus_operation(number_1, number_2):
    return float(number_1) + float(number_2)


def minus_operation(number_1, number_2):
    return float(number_1) - float(number_2)


def multiply_operation(number_1, number_2):
    return float(number_1) * float(number_2)


def divide_operation(number_1, number_2):
    return float(number_1) / float(number_2)
